public class Calculator {
    
    public Calculator(){
    }
    
    public int add(int rightSideOperand, int leftSideOperand){
        return leftSideOperand + rightSideOperand;
    }
    
    public int subtract(int rightSideOperand, int leftSideOperand){
        return leftSideOperand - rightSideOperand;
    }
}
